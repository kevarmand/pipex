#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*temp;

	temp = malloc(sizeof(t_list));
	if (temp == 0)
		return (0);
	temp->content = content;
	temp->next = 0;
	return (temp);
}
